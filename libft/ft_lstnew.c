#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*list;

	list = (t_list *)malloc(sizeof(t_list));
	if (!list)
		return (NULL);
	if (content)
		list->content = content;
	else
		list->content = NULL;
	list->next = NULL;
	return (list);
}
