#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	while (n > 0)
	{
		*d = *s;
		if (*s == (unsigned char)c)
			return ((void *)(d + 1));
		d += 1;
		s += 1;
		n--;
	}
	return (NULL);
}
